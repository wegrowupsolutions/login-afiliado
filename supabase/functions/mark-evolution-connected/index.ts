import { serve } from "https://deno.land/std@0.168.0/http/server.ts"
import { createClient } from 'https://esm.sh/@supabase/supabase-js@2'

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
}

serve(async (req) => {
  // Handle CORS preflight requests
  if (req.method === 'OPTIONS') {
    return new Response('ok', { headers: corsHeaders })
  }

  try {
    const { instanceName, phoneNumber, userId } = await req.json()
    
    console.log('Received connection notification:', { instanceName, phoneNumber, userId })
    
    if (!instanceName) {
      return new Response(
        JSON.stringify({ error: 'instanceName is required' }),
        { 
          status: 400, 
          headers: { ...corsHeaders, 'Content-Type': 'application/json' }
        }
      )
    }

    // Create Supabase client with service role
    const supabaseUrl = Deno.env.get('SUPABASE_URL')!
    const supabaseServiceKey = Deno.env.get('SUPABASE_SERVICE_ROLE_KEY')!
    
    const supabase = createClient(supabaseUrl, supabaseServiceKey)

    // Call the function to mark instance as connected
    const { error } = await supabase.rpc('mark_instance_connected', {
      p_user_id: userId,
      p_instance_name: instanceName,
      p_phone_number: phoneNumber
    })

    if (error) {
      console.error('Error marking instance as connected:', error)
      return new Response(
        JSON.stringify({ error: 'Failed to update instance status' }),
        { 
          status: 500, 
          headers: { ...corsHeaders, 'Content-Type': 'application/json' }
        }
      )
    }

    console.log('Instance marked as connected successfully')
    
    return new Response(
      JSON.stringify({ success: true, message: 'Instance marked as connected' }),
      { 
        status: 200, 
        headers: { ...corsHeaders, 'Content-Type': 'application/json' }
      }
    )

  } catch (error) {
    console.error('Error processing request:', error)
    return new Response(
      JSON.stringify({ error: 'Internal server error' }),
      { 
        status: 500, 
        headers: { ...corsHeaders, 'Content-Type': 'application/json' }
      }
    )
  }
})